const int maxItem = 10000000;
int itemLink[maxItem], itemNext[maxItem], itemStart[500][500];
int itemCount;

class ImageScanner {
private:
	vector<string> board;
	vector<bool> scanned;
	int height, width, nBlack, nLetter;
    
    struct PutItem {
        int x, y, letter, val;
        PutItem(int _x, int _y, int _l) {
            x = _x, y = _y, letter = _l;
        }
        friend bool operator <(const PutItem &a, const PutItem &b) {
            return a.val > b.val;
        }
    };
    
    int count_val(int id, int x, int y, int let) {
        int ans = 0;
        for (int i = 0; i < ldata_h[let]; i ++)
            if (scanned[x+i])
                for (int j = 0, tmp = ldata[let][i]; j < ldata_max_w && y+j < width; j ++, tmp /= 2)
                    if (tmp%2 == 1 && board[x+i][y+j] == '1') {
                        itemLink[++ itemCount] = id;
                        itemNext[itemCount] = itemStart[x+i][y+j];
                        itemStart[x+i][y+j] = itemCount;
                        ans ++;
                    }
        return ans;
    }
    
    vector<PutItem> list;
    
    void erase_p(int x, int y) {
        int i = itemStart[x][y];
        while (i > 0) {
            list[itemLink[i]].val --;
            i = itemNext[i];
        }
    }
    
    void put(int x, int y, int let) {
     //   printf("put: (%d, %d, %d)\n", x, y, let);
        for (int i = 0; i < ldata_h[let]; i ++)
            for (int j = 0, tmp = ldata[let][i]; j < ldata_max_w && y+j < width; j ++, tmp /= 2)
                if (tmp%2 == 1)
                    if (scanned[x+i]) {
                        erase_p(x+i, y+j);
                        board[x+i][y+j] = '#';
                    } else
                        board[x+i][y+j] = '1';
    }
    
    void calculate() {
        //printf("calculating..\n"); fflush(stdout);
        
        for (int x = 0; x < height; x ++) {
            //if (x % 30 == 0) printf("pre x=%d\n",x); fflush(stdout);
            for (int y = 0; y < width; y ++) {
                int first_scanned = -1;
                vector<pair<int, int> > scanned_lines;
                for (int i = 0; i < ldata_max_h; i ++) {
                    if (x+i >= height || !scanned[x+i])
                        continue;
                    if (first_scanned == -1)
                        first_scanned = i;
                    scanned_lines.push_back(make_pair(i, 0));
                    for (int j = 0, tm = 1; j < ldata_max_w && y+j < width; j ++, tm *= 2)
                        if (board[x+i][y+j] == '1')
                            scanned_lines.back().second += tm;
                }
                
                //printf("x=%d y=%d\n", x, y);
                //for (int i = 0; i < ldata_max_h; i++) printf("%d ", br[i]); printf("\n");
                
                for (int let = first_scanned; let < ldata_count; let ++) {
                    if (x+ldata_h[let]-1 >= height || y+ldata_w[let]-1 >= width)
                        continue;
                    bool flag = true;
                   // int *vx = br, *vy = ldata[let], *ve = ldata[let] + ldata_h[let];
                    for (vector<pair<int, int> >::iterator it = scanned_lines.begin(); flag && it != scanned_lines.end(); ++ it) //{
                        if ((it->second | ldata[let][it->first]) != it->second)
                            flag = false;
                    /*
                    for (int i = 0; flag && i < ldata_h[let]; i ++)
                        if (scanned[x+i]  && (br[i] | ldata[let][i]) != br[i])
                            flag = false;
                    */
                    if (flag)
                        list.push_back(PutItem(x, y, let));
                }
                
            }
        }
        printf("list.size = %d\n", list.size());
        
        memset(itemStart, itemCount = 0, sizeof(itemStart));
        for (int i = 0; i < list.size(); i ++)
            list[i].val = count_val(i, list[i].x, list[i].y, list[i].letter);
        printf("itemCount = %d\n", itemCount); fflush(stdout);
        while (nLetter -- > 0 && list.size() > 0) {
            int maxv = -1, maxp;
            for (int i = 0; i < list.size(); i ++)
                if (list[i].val > maxv)
                    maxv = list[i].val, maxp = i;
            put(list[maxp].x, list[maxp].y, list[maxp].letter);
        }        
        
        /*
        while (nLetter -- > 0 && list.size() > 0) {
            int maxv = -1, maxp;
            for (int i = 0; i < list.size(); i ++) {
                int v = count_val(list[i].x, list[i].y, list[i].letter);
                if (v > maxv)
                    maxv = v, maxp = i;
            }
            put(list[maxp].x, list[maxp].y, list[maxp].letter);
            swap(list[maxp], list[list.size()-1]);
            list.pop_back();
        }
        */
        for (int i = 0; i < height; i ++)
            for (int j = 0; j < width; j ++) {
                if (board[i][j] == '#')
                    board[i][j] = '1';
                if (board[i][j] == '?')
                    board[i][j] = '0';
            }
    }
	
public:	
	vector<string> restore(int _h, int _w, int _nb, int _nl) {
        srand(123456789);
		height = _h;
		width = _w;
		nBlack = _nb;
		nLetter = _nl;
		
		board.clear();
		board.resize(height, string(width, '?'));
		scanned.clear();
		scanned.resize(height, false);
		
		vector<int> period;
	//	period.push_back(3);
		period.push_back(3);
		period.push_back(4);
		for (int i = 2, _ = 0; i < height; i += period[_++%period.size()]) {
            scanned[i] = true;
            board[i] = ImageToScan::scan(i);
            for (int j = 0; j < width; j ++)
                if (board[i][j] == '1')
                    nBlack --;
        }
        
        calculate();
        
		return board;
	}
};



